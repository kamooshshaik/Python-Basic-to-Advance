#Type Casting = the process of converting a variable from one data type to another
#                str(), float() , bool(), int()

name = "Shaik Kamoosh"
age = 25
cpi = 8.3
is_student = True

print(type(name))
print(type(age))
print(type(cpi))
print(type(is_student))


# Type cast
# float to -> int
cpi = int(cpi)
print(cpi)

# int to -> float
age = float(age)
print(age)